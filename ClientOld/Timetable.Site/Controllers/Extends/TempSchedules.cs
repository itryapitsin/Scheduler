        public Schedule CreateSchedule(
            int Id,
            string wt,
            string TutorialName,
            string LecturerName,
            string TutorialType,
            int AudId,
            int BuildId,
            string GroupIds,
            string CourseIds,
            int Day,
            int PeriodId,
            int hours,
            int StudyYearId,
            int SemesterId
            ){
                var s1 = new Schedule();
                s1.Id = Id;
                if (AudId != -1)
                {
                    var aController = new AuditoriumController();
                    s1.Auditorium = new Auditorium();

                    if (TutorialType == "л")
                    {
                        s1.Auditorium = aController.CreateAuditorium(AudId, 1, BuildId);
                    }
                    if (TutorialType == "лаб")
                    {
                        s1.Auditorium = aController.CreateAuditorium(AudId, 2, BuildId);
                    }
                    if (TutorialType == "п")
                    {
                        s1.Auditorium = aController.CreateAuditorium(AudId, 4, BuildId);
                    }
                }

                s1.Period = new Time();
                if (PeriodId == 1)
                    s1.Period.Id = 12;
                if (PeriodId == 2)
                    s1.Period.Id = 13;
                if (PeriodId == 3)
                    s1.Period.Id = 14;
                if (PeriodId == 4)
                    s1.Period.Id = 15;
                if (PeriodId == 5)
                    s1.Period.Id = 16;
                if (PeriodId == 6)
                    s1.Period.Id = 17;
                if (PeriodId == 7)
                    s1.Period.Id = 33;

                s1.WeekType = new WeekType();
                if(wt == "Л"){
                    s1.WeekType.Id = 1;
                    s1.WeekType.Name = "Е.";
                }

                if(wt == "Ч"){
                    s1.WeekType.Id = 2;
                    s1.WeekType.Name = "Ч.";
                }

                if(wt == "З"){
                    s1.WeekType.Id = 3;
                    s1.WeekType.Name = "З.";
                }

                s1.DayOfWeek = Day;
                s1.ScheduleInfo = new ScheduleInfo();

                s1.ScheduleInfo.Id = Id;

                
                s1.ScheduleInfo.Semester = SemesterId;
                s1.ScheduleInfo.StudyYear = new StudyYear();
                s1.ScheduleInfo.StudyYear.Id = StudyYearId;
                s1.ScheduleInfo.Tutorial = new Tutorial();
                s1.ScheduleInfo.Tutorial.Id = Id;
                s1.ScheduleInfo.Tutorial.Name = TutorialName;

               s1.ScheduleInfo.TutorialType = new TutorialType();

                if(TutorialType == "л"){
                    s1.ScheduleInfo.TutorialType.Id = 1;
                    s1.ScheduleInfo.TutorialType.Name = "Лекция";
                }
                if (TutorialType == "лаб")
                {
                    s1.ScheduleInfo.TutorialType.Id = 2;
                    s1.ScheduleInfo.TutorialType.Name = "Лабораторная";
                }
                if (TutorialType == "п")
                {
                    s1.ScheduleInfo.TutorialType.Id = 3;
                    s1.ScheduleInfo.TutorialType.Name = "Практика";
                }

                var courses = new List<Course>();
                foreach (var courseId in CourseIds.Split(new[] { ',' }, StringSplitOptions.RemoveEmptyEntries))
                {
                    if (courseId != " ")
                    {
                        int icourseId = int.Parse(courseId);
                        var c = new Course();
                        c.Id = icourseId;
                        courses.Add(c);
                    }
                }

                s1.ScheduleInfo.Courses = courses.ToArray();

                var groups = new List<Group>();
                foreach (var gId in GroupIds.Split(new[] { ',' }, StringSplitOptions.RemoveEmptyEntries))
                {
                    if (gId != " ")
                    {
                        int igId = int.Parse(gId);
                        var g = new Group();
                        g.Id = igId;
                        g.Code = igId.ToString();
                        groups.Add(g);
                    }
                }

                s1.ScheduleInfo.Lecturer = new Lecturer();
                s1.ScheduleInfo.Lecturer.Id = Id;
                s1.ScheduleInfo.Lecturer.Lastname = LecturerName;
                s1.ScheduleInfo.Lecturer.Middlename = "";
                s1.ScheduleInfo.Lecturer.Firstname = "";
               

                s1.ScheduleInfo.Groups = groups.ToArray();

                return s1;
        }
